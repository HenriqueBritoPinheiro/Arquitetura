package br.com.fatec.calculadora.calculadora.model;

public class Calculator {
    public static float sum(float n1, float n2){
        return n1 + n2;
    }

    public static float minus(float n1, float n2){
        return n1 - n2;
    }

    public static float multiplication(float n1, float n2){
        return n1 * n2;
    }

    public static float division(float n1, float n2){
        return n1 / n2;
    }
}
